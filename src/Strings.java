/**
 * Ссылочные типы данных
 */
public class Strings {
    public static void main(String[] args) {
        int x = 5; // Примитивный тип данных
         String s = "Hello"; // переменная S ссылается на объект String "Hello" - это объект
         String space = " ";
         String name = "Bob";
         System.out.println(s+space+name);
         System.out.println("Hellow"+" "+"John");
        System.out.println("My numbrer is "+x);
    }
}
